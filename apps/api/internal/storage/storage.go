// Package storage handles filesystem reads and writes for the CV project.
//
// Security:
//   - All paths are resolved relative to CV_PATH and validated
//   - Application names are checked against a strict regex
//   - No symlink following (filepath.EvalSymlinks prevents escaping)
//   - File reads are bounded by size limits
