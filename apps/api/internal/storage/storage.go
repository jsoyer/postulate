const (
	versionsDir     = ".versions"
	maxNoteVersions = 10
)

// NoteVersion holds metadata about a historical version of notes.md.
type NoteVersion struct {
	Filename  string    `json:"filename"`
	SavedAt   time.Time `json:"saved_at"`
	SizeBytes int64     `json:"size_bytes"`
}

// WriteNotesVersioned writes notes and saves the previous version in a
// .versions/ subdirectory before overwriting. At most 10 versions are kept;
// the oldest are pruned when the limit is exceeded.
func (s *Storage) WriteNotesVersioned(name, content string) error {
	if err := cvutil.ValidateAppName(name); err != nil {
		return err
	}

	_, unlock := s.lockApp(name)
	defer unlock()

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return err
	}

	notesPath := filepath.Join(appDir, notesFile)
	existing, readErr := os.ReadFile(notesPath)

	if readErr == nil && len(existing) > 0 {
		vDir := filepath.Join(appDir, versionsDir)
		if mkErr := os.MkdirAll(vDir, 0750); mkErr != nil {
			return fmt.Errorf("create versions dir for %s: %w", name, mkErr)
		}

		ts := time.Now().UTC().Format("20060102T150405Z")
		vFilename := "notes-" + ts + ".md"
		vPath := filepath.Join(vDir, vFilename)
		if wErr := os.WriteFile(vPath, existing, 0640); wErr != nil {
			return fmt.Errorf("write version snapshot for %s: %w", name, wErr)
		}

		if pErr := s.pruneNoteVersions(vDir); pErr != nil {
			return fmt.Errorf("prune versions for %s: %w", name, pErr)
		}
	}

	if err := os.WriteFile(notesPath, []byte(content), 0640); err != nil {
		return fmt.Errorf("write notes for %s: %w", name, err)
	}
	app, _ := s.readAppMeta(name)
	if app != nil {
		s.idx.Add(name, strings.Join([]string{name, app.Company, app.Position, content}, " "))
	}
	return nil
}

// pruneNoteVersions removes the oldest version files if there are more than
// maxNoteVersions entries in the given directory.
func (s *Storage) pruneNoteVersions(vDir string) error {
	entries, err := os.ReadDir(vDir)
	if err != nil {
		return err
	}

	var names []string
	for _, e := range entries {
		if !e.IsDir() {
			names = append(names, e.Name())
		}
	}
	sort.Strings(names)

	for len(names) > maxNoteVersions {
		oldest := names[0]
		names = names[1:]
		if rmErr := os.Remove(filepath.Join(vDir, oldest)); rmErr != nil {
			return rmErr
		}
	}
	return nil
}

// ListNoteVersions returns version metadata for an application's notes,
// sorted oldest first.
func (s *Storage) ListNoteVersions(name string) ([]NoteVersion, error) {
	if err := cvutil.ValidateAppName(name); err != nil {
		return nil, err
	}

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, err
	}

	vDir := filepath.Join(appDir, versionsDir)
	entries, err := os.ReadDir(vDir)
	if err != nil {
		if os.IsNotExist(err) {
			return []NoteVersion{}, nil
		}
		return nil, fmt.Errorf("read versions dir for %s: %w", name, err)
	}

	var versions []NoteVersion
	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		info, infoErr := e.Info()
		if infoErr != nil {
			continue
		}
		nv := NoteVersion{
			Filename:  e.Name(),
			SavedAt:   info.ModTime().UTC(),
			SizeBytes: info.Size(),
		}
		versions = append(versions, nv)
	}

	sort.Slice(versions, func(i, j int) bool {
		return versions[i].Filename < versions[j].Filename
	})

	if versions == nil {
		return []NoteVersion{}, nil
	}
	return versions, nil
}

// ReadNoteVersion reads a specific historical version of notes by filename.
func (s *Storage) ReadNoteVersion(name, filename string) ([]byte, error) {
	if err := cvutil.ValidateAppName(name); err != nil {
		return nil, err
	}

	filename = filepath.Base(filename)

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, err
	}

	vDir := filepath.Join(appDir, versionsDir)
	vPath := filepath.Join(vDir, filename)

	info, err := os.Stat(vPath)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, fmt.Errorf("version %q not found", filename)
		}
		return nil, fmt.Errorf("stat version file: %w", err)
	}
	if info.Size() > maxFileSize {
		return nil, fmt.Errorf("version file exceeds max size")
	}

	data, err := os.ReadFile(vPath)
	if err != nil {
		return nil, fmt.Errorf("read version %s for %s: %w", filename, name, err)
	}
	return data, nil
}

// UploadFile validates the extension and writes data to the application directory.
func (s *Storage) UploadFile(name, filename string, data []byte) error {
	if err := cvutil.ValidateAppName(name); err != nil {
		return err
	}

	_, unlock := s.lockApp(name)
	defer unlock()

	filename = filepath.Base(filename)
	ext := strings.ToLower(filepath.Ext(filename))
	if !allowedUploadExts[ext] {
		return fmt.Errorf("invalid extension %q: only .txt, .pdf, .md, .yml, .yaml, .docx are allowed", ext)
	}
	if int64(len(data)) > maxFileSize {
		return fmt.Errorf("file exceeds maximum allowed size of %d bytes", maxFileSize)
	}

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return err
	}

	path := filepath.Join(appDir, filename)
	if err := os.WriteFile(path, data, 0640); err != nil {
		return fmt.Errorf("write file %s for %s: %w", filename, name, err)
	}
	return nil
}

// ReadFileRaw returns the raw bytes and content-type of a file in the application directory.
func (s *Storage) ReadFileRaw(name, filename string) ([]byte, string, error) {
	if err := cvutil.ValidateAppName(name); err != nil {
		return nil, "", err
	}

	filename = filepath.Base(filename)
	ext := strings.ToLower(filepath.Ext(filename))
	if !allowedUploadExts[ext] {
		return nil, "", fmt.Errorf("invalid extension %q: file not allowed", ext)
	}

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, "", err
	}

	path := filepath.Join(appDir, filename)
	info, err := os.Stat(path)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, "", fmt.Errorf("file %q not found", filename)
		}
		return nil, "", fmt.Errorf("stat file %s: %w", filename, err)
	}
	if info.Size() > maxFileSize {
		return nil, "", fmt.Errorf("file %s exceeds max size (%d bytes)", filename, maxFileSize)
	}

	data, err := os.ReadFile(path)
	if err != nil {
		return nil, "", fmt.Errorf("read file %s for %s: %w", filename, name, err)
	}

	ct := extContentTypes[ext]
	if ct == "" {
		ct = "application/octet-stream"
	}
	return data, ct, nil
}

// SkillsGap tokenizes job.txt and data/cv.yml, then returns missing and present keywords.
func (s *Storage) SkillsGap(name string) (*models.SkillsGapResponse, error) {
	if err := cvutil.ValidateAppName(name); err != nil {
		return nil, err
	}

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, err
	}

	jobPath := filepath.Join(appDir, "job.txt")
	jobContent, err := s.safeReadFile(jobPath)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, fmt.Errorf("job.txt not found for application %q", name)
		}
		return nil, fmt.Errorf("read job.txt for %s: %w", name, err)
	}

	cvPath := filepath.Join(s.cvPath, "data", "cv.yml")
	cvContent, err := s.safeReadFile(cvPath)
	if err != nil {
		return nil, fmt.Errorf("read cv.yml: %w", err)
	}

	jobTokens := tokenize(jobContent)
	cvTokens := tokenize(cvContent)

	var missing, present []string
	for token := range jobTokens {
		if cvTokens[token] {
			present = append(present, token)
		} else {
			missing = append(missing, token)
		}
	}

	sort.Strings(missing)
	sort.Strings(present)

	return &models.SkillsGapResponse{
		Missing: missing,
		Present: present,
	}, nil
}

// tokenize lowercases text, strips non-alphanumeric characters, and returns
// a set of words with a minimum length of 4 that are not stopwords.
func tokenize(text string) map[string]bool {
	lower := strings.ToLower(text)
	words := strings.Fields(nonAlphaNum.ReplaceAllString(lower, " "))

	set := make(map[string]bool, len(words))
	for _, w := range words {
		if len(w) < 4 {
			continue
		}
		if skillsStopwords[w] {
			continue
		}
		set[w] = true
	}
	return set
}

// searchFiles are the files searched by the Search method.
var searchFiles = []string{"job.txt", "prep.md", "company-research.md", "notes.md"}

// Search performs case-insensitive full-text search across application files.
// Returns up to maxResults matching applications with snippets.
// It uses the in-memory index for candidate selection when the index is non-empty,
// falling back to a linear scan otherwise.
func (s *Storage) Search(query string, maxResults int) (*models.SearchResponse, error) {
	if maxResults <= 0 {
		maxResults = 20
	}

	lowerQuery := strings.ToLower(query)

	// Try the index first to narrow candidates.
	candidates := s.idx.Search(query, maxResults)

	var apps []models.Application
	if len(candidates) > 0 {
		for _, name := range candidates {
			app, err := s.readAppMeta(name)
			if err != nil {
				continue
			}
			apps = append(apps, *app)
		}
	} else {
		// Fall back to linear scan (index empty or no hits).
		all, err := s.ListApplications()
		if err != nil {
			return nil, err
		}
		apps = all
	}

	var results []models.SearchResult

	for _, app := range apps {
		if len(results) >= maxResults {
			break
		}

		appDir, err := s.resolveAppDir(app.Name)
		if err != nil {
			continue
		}

		var matches []models.SearchMatch
		for _, fname := range searchFiles {
			fpath := filepath.Join(appDir, fname)
			content, err := s.safeReadFile(fpath)
			if err != nil {
				continue
			}
			if i := strings.Index(strings.ToLower(content), lowerQuery); i >= 0 {
				snippet := getSnippet(content, i, len(query), 120)
				matches = append(matches, models.SearchMatch{File: fname, Snippet: snippet})
			}
		}

		if len(matches) > 0 {
			results = append(results, models.SearchResult{
				Name:     app.Name,
				Company:  app.Company,
				Position: app.Position,
				Stage:    app.Status,
				Matches:  matches,
			})
		}
	}

	return &models.SearchResponse{Results: results}, nil
}

// getSnippet extracts a context window around a match position.
func getSnippet(text string, matchIdx, matchLen, radius int) string {
	start := matchIdx - radius
	if start < 0 {
		start = 0
	}
	end := matchIdx + matchLen + radius
	if end > len(text) {
		end = len(text)
	}
	snippet := text[start:end]
	if start > 0 {
		snippet = "…" + snippet
	}
	if end < len(text) {
		snippet = snippet + "…"
	}
	return snippet
}

const (
	healthScoresFile    = ".health-scores.yml"
	maxHealthScores     = 30
)

// ScoreEntry records an audit score at a point in time.
type ScoreEntry struct {
	CheckedAt           time.Time `json:"checked_at" yaml:"checked_at"`
	QuantificationScore float64   `json:"quantification_score" yaml:"quantification_score"`
	ActionVerbScore     float64   `json:"action_verb_score" yaml:"action_verb_score"`
	CompletenessScore   float64   `json:"completeness_score" yaml:"completeness_score"`
	OverallScore        float64   `json:"overall_score" yaml:"overall_score"`
}

// AppendHealthScore stores a score entry for the named application.
// At most maxHealthScores entries are kept; oldest entries are dropped when
// the limit is exceeded.
func (s *Storage) AppendHealthScore(name string, entry ScoreEntry) error {
	if err := cvutil.ValidateAppName(name); err != nil {
		return err
	}

	_, unlock := s.lockApp(name)
	defer unlock()

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return err
	}

	scores, _ := s.readHealthScores(appDir)
	scores = append(scores, entry)
	if len(scores) > maxHealthScores {
		scores = scores[len(scores)-maxHealthScores:]
	}

	data, err := yaml.Marshal(scores)
	if err != nil {
		return fmt.Errorf("marshal health scores for %s: %w", name, err)
	}

	path := filepath.Join(appDir, healthScoresFile)
	if err := os.WriteFile(path, data, 0640); err != nil {
		return fmt.Errorf("write health scores for %s: %w", name, err)
	}
	return nil
}

// ListHealthScores returns all stored score entries for the named application.
func (s *Storage) ListHealthScores(name string) ([]ScoreEntry, error) {
	if err := cvutil.ValidateAppName(name); err != nil {
		return nil, err
	}

	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, err
	}

	scores, err := s.readHealthScores(appDir)
	if err != nil {
		return nil, err
	}
	if scores == nil {
		return []ScoreEntry{}, nil
	}
	return scores, nil
}

// readHealthScores reads the .health-scores.yml file for an application directory.
// It silently returns an empty slice when the file does not yet exist.
func (s *Storage) readHealthScores(appDir string) ([]ScoreEntry, error) {
	path := filepath.Join(appDir, healthScoresFile)
	data, err := os.ReadFile(path)
	if err != nil {
		if os.IsNotExist(err) {
			return []ScoreEntry{}, nil
		}
		return nil, fmt.Errorf("read health scores: %w", err)
	}
	var scores []ScoreEntry
	if err := yaml.Unmarshal(data, &scores); err != nil {
		return nil, fmt.Errorf("parse health scores: %w", err)
	}
	if scores == nil {
		return []ScoreEntry{}, nil
	}
	return scores, nil
}

func (s *Storage) readAppMeta(name string) (*models.Application, error) {
	appDir, err := s.resolveAppDir(name)
	if err != nil {
		return nil, err
	}

	metaPath := filepath.Join(appDir, metaFile)
	content, err := s.safeReadFile(metaPath)
	if err != nil {
		return nil, fmt.Errorf("read meta.yml for %s: %w", name, err)
	}

	var meta applicationMeta
	if err := yaml.Unmarshal([]byte(content), &meta); err != nil {
		return nil, fmt.Errorf("parse meta.yml for %s: %w", name, err)
	}

	// Parse created date: try YYYY-MM-DD first, then YYYY-MM (Makefile format)
	createdAt, _ := time.Parse("2006-01-02", meta.Created)
	if createdAt.IsZero() {
		createdAt, _ = time.Parse("2006-01", meta.Created)
	}

	// Status is derived from outcome; default to "applied" if empty
	status := meta.Outcome
	if status == "" {
		status = "applied"
	}

	app := &models.Application{
		Name:      name,
		Company:   meta.Company,
		Position:  meta.Position,
		Status:    status,
		CreatedAt: createdAt,
		Outcome:   meta.Outcome,
	}

	if meta.Deadline != nil {
		if t, err := time.Parse("2006-01-02", *meta.Deadline); err == nil {
			app.Deadline = &t
		}
	}

	return app, nil
}

// resolveAppDir returns the absolute path to an application directory,
// validating it stays within the applications directory (no symlink escape).
func (s *Storage) resolveAppDir(name string) (string, error) {
	appsDir := filepath.Join(s.cvPath, applicationsDir)
	appDir := filepath.Join(appsDir, name)

	resolved, err := filepath.EvalSymlinks(appDir)
	if err != nil {
		if os.IsNotExist(err) {
			return "", fmt.Errorf("application %q not found", name)
		}
		return "", fmt.Errorf("resolve path for %s: %w", name, err)
	}

	resolvedApps, err := filepath.EvalSymlinks(appsDir)
	if err != nil {
		return "", fmt.Errorf("resolve applications dir: %w", err)
	}

	if !strings.HasPrefix(resolved, resolvedApps+string(os.PathSeparator)) {
		return "", fmt.Errorf("path traversal detected for %q", name)
	}

	return resolved, nil
}

// safeReadFile reads a file with size limits.
func (s *Storage) safeReadFile(path string) (string, error) {
	info, err := os.Stat(path)
	if err != nil {
		return "", err
	}
	if info.Size() > maxFileSize {
		return "", fmt.Errorf("file %s exceeds max size (%d bytes)", path, maxFileSize)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}
	return string(data), nil
}

// sanitizeDirName creates a filesystem-safe directory name from a company name.
func sanitizeDirName(name string) string {
	name = strings.ToLower(name)
	name = strings.Map(func(r rune) rune {
		if (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') || r == '-' {
			return r
		}
		if r == ' ' || r == '_' {
			return '-'
		}
		return -1
	}, name)
	for strings.Contains(name, "--") {
		name = strings.ReplaceAll(name, "--", "-")
	}
	name = strings.Trim(name, "-")
	if len(name) > 50 {
		name = name[:50]
	}
	return name
}
